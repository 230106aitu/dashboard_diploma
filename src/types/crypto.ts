/**
 * Saved Analysis Report (persisted to Supabase database)
 * Demonstrates data persistence methodology for the thesis.
 */
export interface AnalysisReport {
  id: string;
  coin_id: string;
  coin_name: string;
  time_range: number;
  title: string;
  notes: string;
  insights: AnalysisInsight[];
  chart_config: {
    showMA7: boolean;
    showMA30: boolean;
    showMA200: boolean;
    showTrendLine: boolean;
    comparisonCoin: string;
  };
  stats_snapshot: DataStats | null;
  created_at: string;
}

/** Lightweight report listing (without full insights payload) */
export interface AnalysisReportSummary {
  id: string;
  coin_id: string;
  coin_name: string;
  time_range: number;
  title: string;
  notes: string;
  chart_config: AnalysisReport['chart_config'];
  created_at: string;
}
